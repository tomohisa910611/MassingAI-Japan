export function localizeProjectDisplayName(displayName: string, language: "en" | "ja") {
  if (language === "ja") return displayName;
  let result = displayName.replace("ボリュームチェック", "MassingCheck");
  for (const [code, japanese] of BUILDING_USES) {
    result = result.replace(`_${japanese}`, `_${BUILDING_USE_ENGLISH[code] ?? japanese}`);
  }
  return result;
}
